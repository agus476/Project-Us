export const DEV_MODE = false;
export const DEV_DATE = null;

export function todayId() {
  if (DEV_MODE && DEV_DATE) return DEV_DATE;
  const today = new Date();
  const year = today.getFullYear();
  const month = String(today.getMonth() + 1).padStart(2, "0");
  const day = String(today.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

export function isAvailable(mission) {
  if (DEV_MODE) return true;
  return mission.date <= todayId();
}

export function getDailyMission(progress) {
  return missions.find((mission) => isAvailable(mission) && !progress.isCleared(mission.id)) || missions[missions.length - 1];
}

export function normalizeCode(value) {
  return String(value)
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, "")
    .trim()
    .toUpperCase();
}
